package com.sopra.eaplanner.notification.reminder;

/**
 * Enum representing the different types of reminders that can be sent before an event.
 * These reminders are based on specific time intervals relative to the event's start time.
 */
public enum ReminderType {
    ONE_WEEK_BEFORE,
    ONE_DAY_BEFORE,
    ONE_HOUR_BEFORE,
}
